package com.radowiecki.transformers.adapter;

public class FleetFactoryAdapter {
	private String fleetTypeName;
	private int level;

	public String getFleetTypeName() {
		return this.fleetTypeName;
	}

	public void setFleetTypeName(String fleetTypeName) {
		this.fleetTypeName = fleetTypeName;
	}

	public int getLevel() {
		return this.level;
	}

	public void setLevel(int level) {
		this.level = level;
	}
}
